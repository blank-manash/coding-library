void test() {

    string s;
    cin >> s;
    int checker = 0;
    for( char ch : s ) {
        int i = ch - 'a';
        if( checker & 1 << i )  {
            cout << ch;
            return; 
        }
        else checker = checker | 1 << i;
    }
}

